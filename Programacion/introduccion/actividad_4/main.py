# Vamos a pedirle al usuario que nos de 3 datos y calcule la media de las 3:

print("Necesitamos que nos de 3 numeros difenentes para poder calular la media de los 3")
a = int(input("Danos el primer numero: "))
b = int(input("Danos el segundo numero: "))
c = int(input("Danos el tercer numero: "))

total = a + b + c
reslutado = total / 3

print("Tu media es: ", reslutado)